
public class DiffVariables {

	public static void main(String[] args) {
		
		double a = 12.234;
		
		short b = 12345;
		
		int c = 'G';
		
		char d = 'D';
		
		boolean Y = true;
		
		long L = 1234243534;
		
		System.out.println(a);
		
		System.out.println(b);
		
		System.out.println(c);
		
		System.out.println(d);
		
		System.out.println(Y);
		
		System.out.println(L);

	}

}
